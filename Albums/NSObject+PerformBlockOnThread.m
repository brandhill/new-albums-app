#import "NSObject+PerformBlockOnThread.h"

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
@implementation NSObject (PerformBlockOnThread)

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)mv_performBlock:(void (^)())block
               onThread:(NSThread*)thread {
  [self performSelector:@selector(mv_performBlock:) onThread:thread
             withObject:block waitUntilDone:NO];
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)mv_performBlockAndWait:(void (^)())block
                      onThread:(NSThread*)thread {
  [self performSelector:@selector(mv_performBlock:) onThread:thread
             withObject:block waitUntilDone:YES];
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)mv_performBlock:(void (^)()) block {
  block();
}

@end
