#import "MVManagedObjectContext.h"

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
@interface MVManagedObjectContext ()
@end

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
@implementation MVManagedObjectContext

@synthesize delegate              = delegate_;

///////////////////////////////////////////////////////////////////////////////////////////////////
- (id)init
{
  self = [super init];
  if(self)
  {
    delegate_ = nil;
  }
  return self;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)mv_save
{
  if([self.delegate respondsToSelector:@selector(managedObjectContextWillSave:)])
    [self.delegate managedObjectContextWillSave:self];
  [super mv_save];
}

@end
